package Oopsconcepts;

public class Arithmatic {
	
	void add(int a,int b)
	{
	 int c = a+b;
	 System.out.println(c);
	}
	void sub(int a,int b)
	{
	 int c = a-b;
	 System.out.println(c);
	}
	public static void main(String[] args)
	{
		Arithmatic ar = new Arithmatic();
		       ar.add(5,0);
		       ar.add(6,0);
		       ar.sub(4,0);
		       ar.sub(6,2);
	}
}
	
	
	
	
	
	
	
	